package PracticeForLoop;

public class whileLoop {
    //While loops take a boolean condition
    public static void main(String[] args) {
        //Declare integer variable a = 0
        int a = 0;
        //Loop while the condition a is less than 100
        while (a<100){
            //Print variable a; increment variable a by 1
            System.out.println(a); a++;
        }
        System.out.println("Loop has ended");
    }
}
