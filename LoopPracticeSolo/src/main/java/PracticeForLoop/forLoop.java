package PracticeForLoop;

public class forLoop {
    public static void main(String[] args) {
        //Create a for loop, requires 3 parameters
        //Inializing variable a is = 0
        //Setting condition the condition that if variable a is less than 100
        //increasing variable a by 1 (increment increase is represented by ++)
        for(int a = 0; a < 100; a++){
            //System will loop through and print out every number between 1-99
            System.out.println(a);
        }
        //System will print "Loop has ended" after 99, the loop is complete the conditions are met.
        System.out.println("Loop has ended");
    }
}
